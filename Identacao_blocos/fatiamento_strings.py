# Fatiamento de strings Ã© uma tecnica ultilizada para retornar substrings (parte da string original)
# informando inicio (start), fim (stop) e passo (step):[start:stop[,step]]

nome = "Anderson Brito de Lima"
print(nome[0])
print(nome[:9])
print(nome[10:])
print(nome[10:16])
print(nome[10:16:2])
print(nome[:])
print(nome[::-1])
