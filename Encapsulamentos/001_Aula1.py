# O que é encapsulamento 
'''
Proteção de acesso
O encapsulamento é uma dos conceitos fundamentais em programação orientada a objetos.
Ele descreve a ideia de agrupar dados e os métodos que manipulam esses dados em uma unidade. 
Isso impõe restrições ao acesso diretoa variáveis e métodos e pode evitar a modificação acidental de dados.
Para evitar alterações acidentais, a variável de uma objeto só pode ser alterada pelo método desse objeto
'''